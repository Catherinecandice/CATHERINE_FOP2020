#include <iostream>
using namespace std;

int main(){
  //variables
  int m = 17, n = 23, o = 49;
  int p;
  double q;

  //calculate to get the total number
  p = m + n + o;
  cout << "the total number = " << p << endl;

  //calculate to get the average number
  q = (m + n + o)/3;
  cout << "the average number = " << q << endl;

  return 0;
}