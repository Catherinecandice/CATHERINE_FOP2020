#include <iostream>
using namespace std;

int main(){
  //variables
  int a = 12, b = 5;
  int addition, subtraction, multiplication, division, remainder;

  //addition
  addition = a + b;
  cout << "addition = " << a + b << endl;

  //subtraction
  subtraction = a - b;
  cout << "subtraction = " << a - b << endl;

  //multiplication
  multiplication = a * b;
  cout << "multiplication = " << a * b << endl;

  //division
  division = a / b;
  cout << "division = " << a / b << endl;

  //remainder
  remainder = a % b;
  cout << "remainder = " << a % b << endl;

  return 0;
}
  